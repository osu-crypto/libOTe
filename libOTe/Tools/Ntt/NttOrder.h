	// Reverse bits in each byte via a 4-bit LUT, then reverse bytes within each 32-bit lane.
	inline __m128i reverse_bits32_ssse3(__m128i x) {
		// 4-bit reversal LUT: 0..15 -> reversed nibble
		const __m128i lut = _mm_setr_epi8(
			0x0, 0x8, 0x4, 0xC, 0x2, 0xA, 0x6, 0xE, 0x1, 0x9, 0x5, 0xD, 0x3, 0xB, 0x7, 0xF
		);
		const __m128i mask_nib = _mm_set1_epi8(0x0F);

		// Reverse bits within each byte (via nibble table)
		__m128i lo = _mm_and_si128(x, mask_nib);
		__m128i hi = _mm_and_si128(_mm_srli_epi16(x, 4), mask_nib);
		lo = _mm_shuffle_epi8(lut, lo);          // low nibble reversed
		hi = _mm_shuffle_epi8(lut, hi);          // high nibble reversed
		__m128i rb = _mm_or_si128(_mm_slli_epi16(lo, 4), hi); // bytes with bits reversed

		// Reverse byte order within each 32-bit lane (NOT across lanes!)
		// (b3 b2 b1 b0) -> (b0 b1 b2 b3) per lane
		const __m128i rev_bytes_per_lane = _mm_setr_epi8(
			3, 2, 1, 0, 7, 6, 5, 4, 11, 10, 9, 8, 15, 14, 13, 12
		);
		return _mm_shuffle_epi8(rb, rev_bytes_per_lane);
	}

	// Reverse the low-k bits of 4 uint32_t values packed in j4.
	// Returns rev32(j) >> (32 - k) in each lane.
	inline __m128i bitrev_k_4_ssse3(__m128i j4, int k) {
		__m128i r = reverse_bits32_ssse3(j4);
		// variable shift count: replicate (32 - k) to the low dword of the count vector
		// (SSE2 uses the low 64 bits of 'count' for all lanes)
		const __m128i sh = _mm_cvtsi32_si128(32 - k);
		return _mm_srl_epi32(r, sh);
	}

	static inline __m128i scale_bitrev4(__m128i r4, uint32_t stride) {
		// double: (2*r)
		__m128i r2 = _mm_slli_epi32(r4, 1);

		// add 1: (2*r + 1)
		__m128i two_r_plus1 = _mm_add_epi32(r2, _mm_set1_epi32(1));

		// broadcast stride
		__m128i stride4 = _mm_set1_epi32(stride);

		// multiply: stride*(2*r+1)
		return _mm_mullo_epi32(two_r_plus1, stride4);
	}

	template<typename F>
	void bitReversePermute(std::span<F> a)
	{
		auto n = a.size();
		const u64 bits = log2ceil(n);
		for (u64 i = 0; i < n; ++i) {
			u64 j = bitReversal(bits, i);
			if (i < j) std::swap(a[i], a[j]);
		}
	}
